<template>
  <div class="list-container">
    <h3>Investment Portfolios</h3>
    <div v-if="loading" class="info-message">Loading portfolios...</div>
    <div v-else-if="portfolios.length === 0" class="info-message error">
      You don't have any portfolios yet. Create one!
    </div>
    <ul v-else>
      <li 
        v-for="portfolio in portfolios" 
        :key="portfolio.id" 
        :class="{ 'selected': portfolio.id === selectedPortfolioId }"
        @click="$emit('select-portfolio', portfolio.id)"
      >
        {{ portfolio.name }} (ID: {{ portfolio.id }})
      </li>
    </ul>
  </div>
</template>

<script lang="ts">
import { defineComponent, ref, onMounted, getCurrentInstance } from 'vue';

interface Portfolio {
  id: number;
  name: string;
}

export default defineComponent({
  name: 'PortfolioList',
  props: {
    selectedPortfolioId: {
      type: [Number, null], 
      default: null 
    }
  },
  emits: ['select-portfolio'],
  setup() {
    const portfolios = ref<Portfolio[]>([]);
    const loading = ref(false);

    const instance = getCurrentInstance();
    const $api = instance?.appContext.config.globalProperties.$api;

    const fetchPortfolios = async () => {
      const token = localStorage.getItem('access_token');
      if (!token) return; 

      loading.value = true;
      try {
        const response = await $api.get('/portfolios', {
          headers: {
            Authorization: `Bearer ${token}`
          }
        });
        portfolios.value = response.data;

      } catch (error) {
        console.error('Błąd ładowania portfeli:', error);
      } finally {
        loading.value = false;
      }
    };

    onMounted(fetchPortfolios);

    return {
      portfolios,
      loading,
      fetchPortfolios,
    };
  },
});
</script>